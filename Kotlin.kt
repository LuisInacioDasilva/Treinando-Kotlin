fun main(){
    
    //variáveis cujo valor pode mudar a qualquer momento
    var vmutavel = "Luis"
    println(vmutavel)
    
    var vmutavel2 = " Silva"
    println(vmutavel2)
    
    var vmutavel3 = vmutavel + vmutavel2
    println(vmutavel3)
    
    //variáveis cujo valor não muda, é constante
    
    val vimutavel = 3.14
    //colocar sempre "." ao invés de ","
    
    var vmutavel4 = 6
    
    var vmutavel5 = vmutavel4 + vimutavel
    
   println(vmutavel5)
}